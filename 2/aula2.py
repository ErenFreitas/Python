# Definindo algumas variáveis booleanas
verdadeiro = True
falso = False

# Imprimindo os valores das variáveis
print("O valor de verdadeiro é:", verdadeiro)
print("O valor de falso é:", falso)

# Convertendo inteiro para float
numero_inteiro = 5
numero_float = float(numero_inteiro)
print("Inteiro " + str(numero_inteiro) + " convertido para float: " + str(numero_float))

# Convertendo float para inteiro
numero_float = 3.14
numero_inteiro = int(numero_float)
print("Float " + str(numero_float) + " convertido para inteiro: " + str(numero_inteiro))
