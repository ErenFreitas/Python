# Em Python, o tipo int representa números inteiros, que podem ser positivos ou negativos. Números inteiros sem sinal são considerados positivos.

print(11) # int
print(-11) # int
print(0) # int

# Em Python, o tipo float representa números com precisão de ponto flutuante. Ele pode representar qualquer número positivo ou negativo com casas decimais.

print(1.1) # float
print(-11.11) # float
print(0.1) # float

# Em Python, str é um tipo de dado que representa uma sequência de caracteres, ou seja, uma cadeia de texto.

# Criando strings
string1 = 'Olá, mundo!'
string2 = "Isto é uma string."

# Imprimindo strings
print(string1)  # Saída: Olá, mundo!
print(string2)  # Saída: Isto é uma string.

# Em Python, a função type() é usada para retornar o tipo de um objeto. Ela recebe um único argumento e retorna o tipo desse argumento.

print(type(5))  # Saída: <class 'int'>

print(type("Olá, mundo!"))  # Saída: <class 'str'>

