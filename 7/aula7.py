#itroduçao ao for
# O loop `for` é usado para iterar sobre uma sequência (como strings, listas, tuplas, etc.).

texto = 'python' 
for letra in texto: 
    print(letra) 

#no exemplo acima, o loop `for` percorre cada caractere da string 'python' e o imprime.

