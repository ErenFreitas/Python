# Introdução aos blocos de código if / elif / else
# if / elif / else

entrada_usuario = input('Você quer "entrar" ou "sair"? ')
if entrada_usuario == 'entrar':
    print('Você entrou no sistema.')
elif entrada_usuario == 'sair':
    print('Você saiu do sistema.')
else:
    print('Você não digitou "entrar" ou "sair".')
