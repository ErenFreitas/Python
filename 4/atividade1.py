# Definindo algumas variáveis para comparação
a = 5
b = 10
c = 5

# Comparações
print("a > b:", a > b)       # False, porque 5 não é maior que 10
print("a < b:", a < b)       # True, porque 5 é menor que 10
print("a >= c:", a >= c)     # True, porque 5 é igual a 5
print("a <= b:", a <= b)     # True, porque 5 é menor que 10
print("a == c:", a == c)     # True, porque 5 é igual a 5
print("a != b:", a != b)     # True, porque 5 é diferente de 10

# Usando operadores de comparação em uma estrutura condicional
if a < b:
    print("a é menor que b")

if a == c:
    print("a é igual a c")

if b > c:
    print("b é maior que c")
